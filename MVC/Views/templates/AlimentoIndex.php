<p class="T2"><a name="P1_23"></a><b>ALIMENTACIÓN DEL CERDO</b></p>

<p class="T4titpar"><b>Fuentes de energía</b></p>

  <ul><p>Los alimentos que se pueden utilizar como fuente de energía son principalmente los granos de cereales, ya sean de maíz blanco o amarillo, sorgo, arroz, trigo, cebada o quinoa. También se pueden utilizar subproductos como el salvado de trigo, papa cocida, plátano maduro y melaza  de caña.</p>

</ul><p class="T4titpar"><b>Fuentes de proteínas</b></p>

  <ul><p>Se pueden emplear como fuentes de proteínas la harina de alfalfa y el gluten de maíz. Sin embargo, las mejores fuentes de proteínas son las harinas de pescado, carne, hueso o sangre, aunque la accesibilidad a estos productos es muy limitada. Otras fuentes de proteínas pueden ser las pastas de oleaginosas como las de soja, algodón, ajonjolí, girasol y cártamo. También se pueden utilizar los desechos de cocina como papa, soja y maní.</p>

</ul><p class="T4titpar"><b>Alimentación del verraco</b></p>

  <ul><p>El cerdo puede aprovechar la mayoría de los alimentos animales y vegetales que se le proporciona. Es recomendable utilizar los alimentos que se produzcan en la localidad. En el caso sea necesario y estén disponibles, se pueden  incorporar mezclas de vitaminas y minerales para asegurar una buena nutrición.</p>

  <p>El plátano de rechazo (verde, maduro o en forma de harina) constituye una buena fuente de alimentación para el cerdo y se lo puede utilizar sin ninguna restricción en todo el ciclo de su vida. </p>

  <p>El consumo de alimento es de aproximadamente 2,5 kg de materia seca por día, lo que representa aproximadamente 10 kg de materia verde.</p>

  <p>Alimentación de las reproductoras</p>

  <p>Durante la primera parte de la gestación el alimento puede ser igual que el suministrado a los verracos. Durante las últimas cuatro semanas su alimentación debe reforzarse con alguna fuente de proteínas,  aumentando la ración de 1,5 kg a 3 kg por día.</p>

  <p>Después del parto se debe proporcionar un alimento rico en proteínas debido a la gran demanda fisiológica que exige la producción de leche.</p>

  <p>Los lechones empezarán a consumir alimento copiando la conducta de la madre; es recomendable que éstos dispongan de alimento molido.</p>

</ul><p class="T4titpar"><b>Alimento de destetados</b></p>

<ul><p>El alimento puede ser hecho a base de granos con alguna fuente de proteínas de la que se disponga en la zona y puede complementarse con pastoreo en pastos frescos como alfalfa y vicia villosa.</p></ul>
