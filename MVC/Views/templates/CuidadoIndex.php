<h2 style="text-transform: uppercase;">PRODUCCIÓN DE LECHE DE LA CERDA SEGÚN EL TAMAÑO DE LA CAMADA</h2>

<p><img src="/images/articulos/2019/01/cerdos/lechon-produccion-leche-tamano.jpg" alt="Manejo del lechón recién nacido" title="Manejo del lechón recién nacido"></p>
<p>&nbsp;</p>

<p>En razas de buena habilidad materna, la producción de leche de una cerda, tomando en cuenta el tamaño de la camada, oscila entre los <strong>8,5 kg y 13,2 kg al día.</strong> En algunas investigaciones se calculó que un lechón a las seis semanas de lactación debe consumir de <strong>40 a 45 kg de leche.</strong></p>

<p>La tomas de leche de los lechones dependen de los días de lactancia de la madre. En las primeras <strong>15 horas</strong> maman alrededor de <strong>15 veces;</strong> en las semanas tres y cuatro maman cada hora, aumentando los intervalos por las noches. En las semanas cinco y seis, maman cada <strong>95 a 120 minutos.</strong></p>

<p>&nbsp;</p>
<p><img src="/images/articulos/2019/01/cerdos/lechon-por-cerda.jpg" alt="Manejo del lechón recién nacido" title="Manejo del lechón recién nacido"></p>
<p>&nbsp;</p>

<p><strong>La cantidad de lechones por cerda no debe superar el número de pezones funcionales.</strong> De ser así el mismo día del parto, hay que incorporar los sobrantes a cerdas nodrizas que parieron pocos lechones. Los amamantamientos de lechones débiles deben priorizarse ubicándolos en un pezón de fácil acceso.</p>

