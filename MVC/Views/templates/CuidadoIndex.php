		<p class="intro">Los lechones recién nacidos son una prioridad dentro de la granja porcina. En esta etapa se presentan los niveles de mortalidad más altos en la piara debido a las condiciones de manejo y enfermedades.</p>
 

<p>El buen manejo al nacer, <strong>tomar calostro en las primeras horas de vida y evitar la infección</strong> del ombligo por la posibilidad de enfermedades o hernias, es determinante para su supervivencia y desempeño productivo en las etapas posteriores.</p>

<p>También es importante el <strong>corte de colmillos</strong> que se realiza con el fin de evitar lesiones por las frecuentes peleas en el establecimiento del pezón definitivo. <strong>Los cuatro pares de dientes de aguja de los lechones son muy filosos al nacer y presentan riesgo de cortaduras y mastitis</strong> en la glándula mamaria, así como de cortes en la cara, orejas, cola y cuerpo de los lechones de la misma camada.</p>

<p>Hay estudios que recomiendan no cortarlos, <strong>evitando infecciones</strong> por una mala práctica. Cada granja es un mundo diferente y se aconseja adaptarse a lo que resulte mejor.</p>

