<h2 style="text-transform: uppercase;">Prácticas necesarias durante el nacimiento</h2>

<p><strong>El parto de las cerdas dura de tres a tres horas y media,</strong> con intervalos de nacimiento de <strong>15 minutos</strong> entre cada lechón. En el momento del parto es necesario asistir a la cerda tomando en cuenta medidas como lavado y desinfección de manos, o el uso de guantes estériles.</p>

<p>La sala de maternidad, donde va a parir la cerda, debe limpiarse y desinfectarse con <strong>15 días</strong> de anticipación. Si esta sala tiene piso y paredes de concreto, es necesario usar detergentes y desinfectantes. La cerda debe llegar de <strong>8 a 10 días</strong> antes de la fecha programada de parto y se le debe proveer una cama seca y limpia.</p>

<p>&nbsp;</p>
<p><img src="/images/articulos/2019/01/cerdos/lechon-cuidados.jpg" alt="Manejo del lechón recién nacido" title="Manejo del lechón recién nacido"></p>
<p>&nbsp;</p>

<p>Al momento del nacimiento se debe contar con una caja o <strong>lechonera de 1x1 metros y 45 o 50 centímetros</strong> de alto, hecha de materiales aislantes como madera, que no se caliente ni absorba el calor del calefactor. El interior debe contener una <strong>cama seca de paja o aserrín</strong> que logre mantener la temperatura. Esta caja servirá para alojar los lechones de una camada para que reciban calor artificial, se concentren en un mismo espacio y sea más fácil su manejo postparto.</p>

<p>&nbsp;</p>
<h2 style="text-transform: uppercase;">CUIDADOS DEL LECHÓN</h2>

<p>• Hay que asegurarse que cada lechón respire al nacer. Se lo puede <strong>estimular secándolo</strong> con una toalla o trapo, priorizando las fosas nasales y boca, que vienen cubiertas con mucosa y membrana placentaria.<br>
• Si algún lechón tiene <strong>problemas de respiración,</strong> es necesario animarlos levantándolos de las patas, haciendo movimientos de abajo hacia arriba o a los lados.<br>
• Realizar el <strong>corte del cordón umbilical</strong> y desinfección del ombligo.<br>
• Luego, se ingresa al lechón a la lechonera, la cual debe estar provista con una lámpara de calor o bombillo incandescente. Se requiere una temperatura entre <strong>32º C y 35° C</strong> en el ambiente.<br>
• Se debe realizar el mismo procedimiento para cada uno de los lechones.<br>
• En la tarjeta de partos se debe <strong>registrar la cantidad de lechones hembras y machos,</strong> así como el número de lechones nacidos muertos por momificación, insuficiencia en el parto u otras causas.</p>

<p>&nbsp;</p>
<p><img src="/images/articulos/2019/01/cerdos/lechon-cuidados-2.jpg" alt="Manejo del lechón recién nacido" title="Manejo del lechón recién nacido"></p>

<p>• Acceso y reconocimiento de pezones entre los <strong>30 y 40 minutos</strong> posparto.<br>
• Para la <strong>toma de calostro</strong> se debe ingresar a todos los lechones al mismo momento para que seleccionen la teta de la que mamarán a lo largo de la lactancia.<br>
• Es recomendable ayudar a los <strong>lechones de poco peso y débiles</strong> a encontrar el pezón para enseñarles a mamar de la madre.<br>
• El corte de colmillo se realiza en las <strong>primeras 6 horas,</strong> después de mamar tres o cuatro veces el calostro de la madre. En este momento los lechones ya han adquirido inmunidad por el consumo de inmunoglobulinas del calostro.<br>
• En el segundo o tercer día de vida del lechón <strong>se aplica de hierro dextrano,</strong> se cortan las colas y se identifica a los lechones por muescas o cualquier otro método.<br>
• La <strong>castración</strong> se realiza en los primeros <strong>10 días.</strong></p>

